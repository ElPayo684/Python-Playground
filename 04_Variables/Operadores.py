x = 6
y = 2
z = 7

print(f"{x} + {y} = { x + y }") # Suma
print(f"{x} - {y} = { x - y }") # Resta
print(f"{x} * {y} = { x * y }") # Multiplicacion
print(f"{x} / {y} = { x / y }") # Division
print(f"{z} / {y} = { z / y }") # Division con resultado float
print(f"{z} // {y} = {z // y}") # Division con resultado en int
print(f"{z} % {y} = {z % y}") # Modulo
print(f"{x} % {y} = {x % y}") # Modulo
print(f"{x} ^ {y} = {x ** y}") # Cuadrado
print(f"{x**y} raiz cuadrada = {(x**y)**0.5}") # Cuadrado